    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        String username = textUsername.getText().trim();
        String password = textPassword.getText().trim();
        
        if (password.equals("")||username.equals("")){
            JOptionPane.showMessageDialog(null, "Cek Input Anda");
        }else{
            try{
                Connection  c = Connector.getKoneksi();
                String regist = "INSERT INTO `users`(`username`,`password`) VALUES ('"+getUsername()+"','"+getPassword()+"')";
                PreparedStatement p = c.prepareStatement(regist);
                p.executeUpdate();
                p.close();
                JOptionPane.showMessageDialog(null, "Berhasil Membuat Akun");
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null, "Username Telah Dipakai!");
                  System.out.println(e.getMessage());  
            }finally{
                this.dispose();
                Login a = new Login();
                a .setVisible(true);
            }
        
    }
    }//GEN-LAST:event_btnRegisterActionPerformed

